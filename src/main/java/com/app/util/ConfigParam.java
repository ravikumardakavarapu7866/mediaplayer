package com.app.util;

public class ConfigParam {
	private String providerId;
	private String configKey;
	private String configValue;
	private String selectVideo;
	private String gridHeight;

	public String getProviderId() {
		return providerId;
	}

	public void setProviderId(String providerId) {
		this.providerId = providerId;
	}

	public String getConfigKey() {
		return configKey;
	}

	public void setConfigKey(String configKey) {
		this.configKey = configKey;
	}

	public String getConfigValue() {
		return configValue;
	}

	public void setConfigValue(String configValue) {
		this.configValue = configValue;
	}

	public String getSelectVideo() {
		return selectVideo;
	}

	public void setSelectVideo(String selectVideo) {
		this.selectVideo = selectVideo;
	}

	public String getGridHeight() {
		return gridHeight;
	}

	public void setGridHeight(String gridHeight) {
		this.gridHeight = gridHeight;
	}

}
